		>.
	</p>

	<p>This project is still under development: the <code>todos</code> section doesn't work!</p>

	<p>To create a new project based on this template using degit:</p>

	<div>
		<code> npx degit el3um4s/memento-sveltekit-electron-typescript </code> <br />
		<code> cd svelte-app </code>
	</div>

	<p>Then install the dependencies with</p>

	<code>npm install</code>

	<p>
		<i
			>Note that you will need to have <a href="https://nodejs.org/" target="_blank">Node.js</a> installed.</i
		>
	</p>
